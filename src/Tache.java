
public class Tache {
	int num;
	int p;
	int d;
	int w;
	
	public Tache(int num, int p, int d, int w) {
		this.num = num;
		this.p = p;
		this.d = d;
		this.w = w;
	}
	
	public int getP() {
		return p;
	}
	
	public int getD() {
		return d;
	}
	
	public int getW() {
		return w;
	}

	public int getNum() {
		return num;
	}
}
